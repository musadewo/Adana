    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if(tf_supid.getText().isEmpty()|| tf_supname.getText().isEmpty()|| tf_supphone.getText().isEmpty()|| 
                tf_supcom.getText().isEmpty()|| tf_address.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Fill all the Fields");
        }
        else{
            try{
                Con = (Connection) (DriverManager.getConnection("jdbc:mysql://localhost:3306/adanadb","root",""));
                PreparedStatement add = Con.prepareStatement("insert into supplier(supid,supname,supphone,"
                        + "supcom,category,supaddress values (?,?,?,?,?,?)");
                add.setString(1, tf_supid.getText());
                add.setString(2, tf_supname.getText());
                add.setString(3, tf_supphone.getText());
                add.setString(4, tf_supcom.getText());
                add.setString(5, cb_category.getSelectedItem().toString());
                add.setString(6, tf_address.getText());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data added Successfully"); 
                tf_supid.setText("");
                tf_supname.setText("");
                tf_supphone.setText("");
                tf_supcom.setText("");
                tf_address.setText("");
                tf_supid.requestFocus();
                isidatasupplier();
            }catch(SQLException e){
            }
        }
    }//GEN-LAST:event_btn_addActionPerformed

