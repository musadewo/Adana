    public void isidatasupplier(){
        int no;
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/adanadb","root","");
            St = Con.createStatement();
            Rs = St.executeQuery("select * from adanadb.supplier");
            ResultSetMetaData RSMD = (ResultSetMetaData) Rs.getMetaData();
            no = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) tb_list.getModel();
            DFT.setRowCount(0);
            while (Rs.next()) {
                Vector v = new Vector();
                for (int i = 1; i <= no; i++) {
                    v.add(Rs.getString("supid"));
                    v.add(Rs.getString("supname"));
                    v.add(Rs.getString("supphone"));
                    v.add(Rs.getString("supcom"));
                    v.add(Rs.getString("category"));
                    v.add(Rs.getString("supaddress"));
                }
                DFT.addRow(v);
            }
        } catch (Exception e) {
        }
    }
    
